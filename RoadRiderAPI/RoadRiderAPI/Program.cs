using RoadRiderAPI;

var builder = WebApplication.CreateBuilder(args);
Dependencies.RegisterServices(builder.Services);
var app = builder.Build();
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
